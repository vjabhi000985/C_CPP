#include<iostream>
using namespace std;
int main()
{
  /* Sum of two numbers */
  // Declaring the variables.
  int x,y;
  
  //Taking input the value of x and y.
  cout<<"Enter the first number:";
  cin>>x;
  
  cout<<"Enter the second number:";
  cin>>y;
  
  // Summation function.
  int sum = x + y;
  
  // Printing the summation value.
  cout<<"The sum of first and second number is:"<<sum;
   
  return 0;
}

